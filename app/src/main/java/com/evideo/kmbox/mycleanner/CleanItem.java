package com.evideo.kmbox.mycleanner;

public class CleanItem {

    private int state = 1;

    private String name = "";

    private String path = "";

    private long size = 0;

    public CleanItem(){}

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }
}
